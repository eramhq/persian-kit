    public static function validate(string $input): ValidationResult
    {
        $input = trim($input);
        $input = DigitConverter::toEnglish($input);
        $input = preg_replace('/[\s\-]/', '', $input);

        if ($input === '') {
            return ValidationResult::failure('کد ملی نمی‌تواند خالی باشد');
        }

        // Left-pad to 10 digits for 8-9 digit inputs
        if (strlen($input) >= 8 && strlen($input) < 10) {
            $input = str_pad($input, 10, '0', STR_PAD_LEFT);
        }

        if (!preg_match('/^\d{10}$/', $input)) {
            return ValidationResult::failure('کد ملی باید ۱۰ رقم باشد');
        }

        // Reject all-same-digit patterns (0000000000 through 9999999999)
        if (preg_match('/^(\d)\1{9}$/', $input)) {
            return ValidationResult::failure('کد ملی نامعتبر است');
        }

        // Reject sequential pattern 0123456789
        if ($input === '0123456789') {
            return ValidationResult::failure('کد ملی نامعتبر است');
        }

        // Check middle 6 digits (positions 3-8) not all zeros
        if (substr($input, 3, 6) === '000000') {
            return ValidationResult::failure('کد ملی نامعتبر است');
        }

        $prefix = substr($input, 0, 3);
        if (!isset(self::CITY_CODES[$prefix])) {
            return ValidationResult::failure('کد ملی نامعتبر است');
        }

        // Checksum validation
        $sum = 0;
        for ($i = 0; $i < 9; $i++) {
            $sum += (int) $input[$i] * (10 - $i);
        }

        $remainder = $sum % 11;
        $checkDigit = (int) $input[9];

        if ($remainder < 2) {
            $valid = $checkDigit === $remainder;
        } else {
            $valid = $checkDigit === (11 - $remainder);
        }

        if (!$valid) {
            return ValidationResult::failure('کد ملی نامعتبر است');
        }

        // City lookup
        $cityData = self::CITY_CODES[$prefix] ?? null;

        return ValidationResult::success([
            'city_code' => $prefix,
            'city'      => $cityData['city'] ?? null,
            'province'  => $cityData['province'] ?? null,
        ]);
    }
